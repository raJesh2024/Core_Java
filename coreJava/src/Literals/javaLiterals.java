package Literals;

public class javaLiterals {

	public static void main(String[] args) {
		
		
		//integer literals
		int a=20;
		System.out.println(a);
		
		
		//floating literals
		float f=20.0f;
		System.out.println(f);
		
		double d =10.0;
		System.out.println(d);
		
		
		//boolean literals
		
		boolean b= true;
		System.out.println(b);
		
		//char literals
		
		
		char ch='a';
		System.out.println(ch);
		
		char ch1=97;
		System.out.println(ch1);
		
		
		//binary ,octal,hexadecimal
		  
		
		//binary base 2,prefix 0b,(0and 1)
		int i=0b1100;
		System.out.println(i);
		
		//octal base 8,prefix 0,(0 to 7)
		
		int k=0123;
		System.out.println(k);
		
		//hexadecimal base16,prefix 0x,(a to f,0 to9)
		
		
		int r=0xef;
		System.out.println(r);
		
	}

}
