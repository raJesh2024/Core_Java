package operatorsJava;

public class unaryOperators {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println(a++);//10
		System.out.println(b++);//20
		 System.out.println(a);
		 System.out.println(b);
		//post increment(increase later)
		
		 a=15;
		 b=30;
		System.out.println(++a);
		System.out.println(++b);
		System.out.println(a);
		System.out.println(b);

	}

}
